package java_OOPS_Features;

public class Inherit_Child {
	
	public int add(int a, int b) {
		int result= a+b;
		return result;
	}
	
	public int sub(int a, int b) {
		int result= a-b;
		return result;
	}
	
	public int multi(int a, int b) {
		int result= a*b;
		return result;
	}
	
	public int div(int a, int b) {
		int result= a/b;
		return result;
	}
	
}
